package ch.teko.game.controllers;

/**
 * The {@code Keys} enum represents the different keys used in the game.
 */
public enum Keys {

    /**
     * Represents the key used to move the player to the right.
     */
    RIGHT,

    /**
     * Represents the key used to move the player to the left.
     */
    LEFT,

    /**
     * Represents the key used to move the player down.
     */
    DOWN,

    /**
     * Represents the key used to move the player up (jump).
     */
    UP,

    /**
     * Represents the key used to perform the player's first attack action.
     */
    ATTACK1,

    /**
     * Represents the key used to perform the player's second attack action.
     */
    ATTACK2,
}