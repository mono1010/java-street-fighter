package ch.teko.game.controllers;

/**
 * Represents player controls for movement and attacks.
 */
public class PlayerControlls {
    /**
     * Indicates whether the player is moving up.
     */
    public boolean up;

    /**
     * Indicates whether the player is moving down.
     */
    public boolean down;

    /**
     * Indicates whether the player is moving right.
     */
    public boolean right;

    /**
     * Indicates whether the player is moving left.
     */
    public boolean left;

    /**
     * Indicates whether the player is performing attack 1.
     */
    public boolean attack1;

    /**
     * Indicates whether the player is performing attack 2.
     */
    public boolean attack2;

    /**
     * Constructs a new instance of the PlayerControls class.
     */
    public PlayerControlls() {
        
    }
}
